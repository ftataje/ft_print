#include "ft_printf.h"

int	ft_print_d_i(int n)
{
	int	len;

	len = 0;
	if (n == -2147483648)
		return (ft_print_s("-2147483648"));
	if (n < 0)
	{
		len += write (1, "-", 1);
		n *= -1;
	}
	if (n >= 10)
		len += ft_print_d_i(n / 10);
	n = n % 10;
	if (n < 10)
		len += ft_print_c(n + 48);
	return (len);
}
